package web.liu.vo;

public class LearnPoint {
	
	private String learnId;   //知识点Id
	private String bookName;	//教材名称
	private String learnPointDesc;	//知识点简介
	public String getLearnId() {
		return learnId;
	}
	public void setLearnId(String learnId) {
		this.learnId = learnId;
	}
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getLearnPointDesc() {
		return learnPointDesc;
	}
	public void setLearnPointDesc(String learnPointDesc) {
		this.learnPointDesc = learnPointDesc;
	}
	
	

}
